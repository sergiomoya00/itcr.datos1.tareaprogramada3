package tareaprogramada3.datos1.administration;

/**
 *
 * @author
 */
public class Route {

    private String origin1;
    private String destiny1;
    private String distance;
    private String time;

    public Route() {
    }

    public String getOrigin1() {
        return origin1;
    }

    public void setOrigin1(String origin1) {
        this.origin1 = origin1;
    }

    public String getDestiny1() {
        return destiny1;
    }

    public void setDestiny1(String destiny1) {
        this.destiny1 = destiny1;
    }

    
    
    public String getDistance() {
        return distance;
    }

    public String getTime() {
        return time;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }

    public void setTime(String time) {
        this.time = time;
    }

}
